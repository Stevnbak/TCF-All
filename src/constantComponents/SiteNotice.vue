<template>
	<div class="site-notice" v-if="message != ''">
		<div class="site-notice-content">
			<p v-html="markdownToHtml"></p>
		</div>
	</div>
</template>

<script>
	import {marked} from "marked";

	export default {
		name: "SiteNotice",
		data: () => ({
			message: "" //"To celebrate our 1 year anniversary, we are hosting a [giveaway](https://thecyclefrontier.wiki/wiki/Wiki:One_Year_Giveaway)"
		}),
		computed: {
			markdownToHtml() {
				return marked(this.message);
			}
		}
	};
</script>

